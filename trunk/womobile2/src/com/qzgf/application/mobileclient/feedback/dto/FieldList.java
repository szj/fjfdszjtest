package com.qzgf.application.mobileclient.feedback.dto;

public class FieldList {
	private String _FieldName; // 字段名称
	private String _FieldValue; // 字段值
	private String _FieldType;//字段类型
	
	public String get_FieldName() {
		return _FieldName;
	}
	public void set_FieldName(String fieldName) {
		_FieldName = fieldName;
	}
	public String get_FieldValue() {
		return _FieldValue;
	}
	public void set_FieldValue(String fieldValue) {
		_FieldValue = fieldValue;
	}
	public String get_FieldType() {
		return _FieldType;
	}
	public void set_FieldType(String fieldType) {
		_FieldType = fieldType;
	}
		
	
}
